#import "CoinbaseObject.h"

@implementation CoinbaseObject

-(id) initWithDictionary:(NSDictionary*)dictionary
{
    return [self initWithDictionary:dictionary];
}

-(id) initWithDictionary:(NSDictionary*)dictionary client:(Coinbase *)client
{
    return [self initWithDictionary:dictionary client:client];
}

-(id) initWithArray:(NSArray*)array
{
    return [self initWithArray:array];
}

-(id) initWithID:(NSString *)theID client:(Coinbase *)client
{
    return [self initWithID:theID client:client];
}

@end
